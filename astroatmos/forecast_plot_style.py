"""
Matplotlib style parameters for adjusting the look of forecast plots.
"""
from astropy.visualization import astropy_mpl_style

# adjust plotting style
light_gray = '#BBBBBB'
astropy_mpl_style['figure.facecolor'] = '#202a37'
astropy_mpl_style['axes.facecolor'] = '#EEEEEE'
astropy_mpl_style['axes.labelcolor'] = light_gray
astropy_mpl_style['axes.titlecolor'] = light_gray
astropy_mpl_style['text.color'] = light_gray
astropy_mpl_style['xtick.color'] = light_gray
astropy_mpl_style['ytick.color'] = light_gray
